// Converts work/dialogue_templates.json (workflow-authored) into the typed
// bank module, validating tones, roles, topics, and placeholders.
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const sourcePath = path.join(root, "work", "dialogue_templates.json");
const outPath = path.join(root, "src", "engines", "dialogue", "banks", "generated.ts");

const TONES = new Set([
  "idle", "walking", "thinking", "coding", "debating", "whispering",
  "drinking_tea", "checking_chart", "excited", "angry", "tired", "confused"
]);
const WHO = new Set(["strategy", "code", "risk", "skeptic", "manager", "data", "target", "witness"]);
const TOPICS = new Set([
  "proposing", "idea_reveal", "data_check", "coding", "backtesting",
  "risk_review", "debate", "decision", "saved", "gossip", "boss_ack", "love", "whip"
]);
const SPOTS = new Set([
  "whiteboard", "meeting", "tea", "data_cabinet", "leaderboard",
  "backtest_computer", "workstations", "window", "none", "manager_desk"
]);
const PLACEHOLDERS = new Set([
  "sharpe", "deflated", "trials", "corr", "cost", "dd", "ret", "turnover",
  "family", "name", "universe", "holding", "params", "gen", "lesson",
  "suggestion", "boss", "reason1", "reason2", "failList", "warnList",
  "checksPassed", "checksTotal", "targetName"
]);
const STATUSES = new Set(["candidate", "rejected", "retest_needed", "failed_to_run", "archived"]);

const raw = JSON.parse(fs.readFileSync(sourcePath, "utf8"));
const templates = raw.templates ?? raw;
const seenKeys = new Set();
const kept = [];
const rejected = [];

for (const template of templates) {
  const problems = [];
  if (!template.key || seenKeys.has(template.key)) problems.push("dup/missing key");
  if (!TOPICS.has(template.topic)) problems.push(`bad topic ${template.topic}`);
  if (!SPOTS.has(template.spot)) problems.push(`bad spot ${template.spot}`);
  if (!Array.isArray(template.lines) || template.lines.length < 2 || template.lines.length > 5) problems.push("bad line count");

  const cond = template.cond ?? undefined;
  if (cond?.statuses && !cond.statuses.every((status) => STATUSES.has(status))) problems.push("bad statuses");
  if (cond?.targetRole && !WHO.has(cond.targetRole)) problems.push("bad targetRole");

  for (const line of template.lines ?? []) {
    if (!WHO.has(line.who)) problems.push(`bad who ${line.who}`);
    if (!TONES.has(line.tone)) problems.push(`bad tone ${line.tone}`);
    if (typeof line.en !== "string" || line.en.length < 2) problems.push("missing en");
    if (typeof line.zh !== "string" || line.zh.length < 2) problems.push("missing zh");
    if (!/[一-鿿]/.test(line.zh ?? "")) problems.push("zh has no CJK");
    for (const text of [line.en ?? "", line.zh ?? ""]) {
      for (const match of text.matchAll(/\{([a-zA-Z0-9]+)\}/g)) {
        if (!PLACEHOLDERS.has(match[1])) problems.push(`unknown placeholder {${match[1]}}`);
      }
    }
    if ((line.who === "target" || line.who === "witness") && !["love", "whip"].includes(template.topic)) {
      problems.push("target/witness outside love/whip");
    }
  }

  if (problems.length > 0) {
    rejected.push({ key: template.key, problems: [...new Set(problems)] });
    continue;
  }
  seenKeys.add(template.key);
  kept.push({
    key: template.key,
    topic: template.topic,
    spot: template.spot,
    weight: Math.min(3, Math.max(1, Number(template.weight) || 1)),
    ...(cond ? { cond } : {}),
    lines: template.lines.map((line) => ({ who: line.who, tone: line.tone, en: line.en, zh: line.zh }))
  });
}

const byTopic = {};
for (const template of kept) byTopic[template.topic] = (byTopic[template.topic] ?? 0) + 1;

const banner = `import type { BankTemplate } from "../dialogueBank";

// Authored dialogue template bank (${kept.length} templates, ${kept.reduce((sum, t) => sum + t.lines.length, 0)} bilingual lines).
// Regenerated by scripts/build-dialogue-banks.mjs from work/dialogue_templates.json - do not edit by hand.
export const DIALOGUE_TEMPLATES: BankTemplate[] = `;

fs.writeFileSync(outPath, banner + JSON.stringify(kept, null, 2) + ";\n", "utf8");
console.log(`kept ${kept.length} templates:`, JSON.stringify(byTopic));
if (rejected.length > 0) {
  console.log(`rejected ${rejected.length}:`);
  for (const item of rejected) console.log(" -", item.key, item.problems.join("; "));
}
